    .text
    .globl _start

    .equ LED_ADDR, 0xFFFF0000

_start:
    # ---------------------------------------------------------
    # 1. LED 寄存器位置放到 s3 = 0xFFFF0000
    # ---------------------------------------------------------
    lui   s3, 0xFFFF0        # s3 = 0xFFFF0000

    # ---------------------------------------------------------
    # 2. 費氏數列初始值
    #    F(0)=0, F(1)=1
    # ---------------------------------------------------------
    li    t0, 0              # t0 = F(n-2)
    li    t1, 1              # t1 = F(n-1)

    # ---------------------------------------------------------
    # 3. 要計算 F(10)，令迴圈跑 9 次
    # ---------------------------------------------------------
    li    t2, 9              # loop count = 9
    li    t3, 0              # counter = 0

fib_loop:
    add   t4, t0, t1         # t4 = F(n) = t0 + t1
    mv    t0, t1             # t0 = F(n-1)
    mv    t1, t4             # t1 = F(n)
    addi  t3, t3, 1          # counter++

    blt   t3, t2, fib_loop   # 若 counter < 9 → 跳回 loop

    # ---------------------------------------------------------
    # 4. 檢查結果是否等於 55
    # ---------------------------------------------------------
    li    t5, 55
    bne   t1, t5, fail       # 如果 t1 != 55 → FAIL

pass:
    li    t6, 1              # PASS code = 1
    sw    t6, 0(s3)          # LED = 1
    j     pass                # 無限迴圈

fail:
    li    t6, 2              # FAIL code = 2
    sw    t6, 0(s3)          # LED = 2
    j     fail                # 無限迴圈
